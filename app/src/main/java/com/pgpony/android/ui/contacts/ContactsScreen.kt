// ContactsScreen.kt
// PGPony Android — Phase A0 + A9
//
// Contacts tab UI — shows device contacts with linked PGP keys,
// auto-match, and keyserver discovery. Matches iOS ContactsView.
//
// Phase A9 additions:
//   • Search bar at top of contact list (OutlinedTextField with
//     leading magnifier + trailing X-to-clear), filters both
//     linked and unlinked sections via VM searchQuery state.
//   • ContactAvatar — 44dp/40dp circular avatars on every row,
//     either the device contact photo or a gradient-with-initials
//     fallback. Color of fallback signals linked-vs-unlinked at
//     a glance.
//   • Trust badge per linked key — small icon (HelpOutline /
//     Warning / VerifiedUser / WorkspacePremium) tinted by
//     trust level (gray / yellow / green / blue), matches iOS
//     trustColor mapping.
//   • UnlinkedContactRow "already checked" indicator — when an
//     email is in state.checkedEmails (returned no key on a
//     prior lookup), the search-icon button is replaced with a
//     muted RemoveCircle to avoid re-checking emails the user
//     already knows are empty.

