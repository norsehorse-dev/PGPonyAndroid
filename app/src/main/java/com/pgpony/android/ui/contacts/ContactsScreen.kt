@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun ContactsScreen(viewModel: ContactsViewModel) {
    val state by viewModel.state.collectAsState()

    val snackbarHostState = remember { SnackbarHostState() }
    LaunchedEffect(state.errorMessage) {
        state.errorMessage?.let { snackbarHostState.showSnackbar(it); viewModel.clearError() }
    }
    LaunchedEffect(state.successMessage) {
        state.successMessage?.let { snackbarHostState.showSnackbar(it); viewModel.clearSuccess() }
    }
    LaunchedEffect(Unit) { viewModel.checkAuthorization() }

    // Phase A9 Fix1 — permission flow now routes through MainActivity
    // because Compose's rememberLauncherForActivityResult is broken on
    // FragmentActivity-hosted activities (request codes generated by
    // the registry exceed FragmentActivity's 16-bit
    // validateRequestPermissionsRequestCode limit and crash with
    // IllegalArgumentException). The helper calls
    // ActivityCompat.requestPermissions with a known small request
    // code and dispatches the result through onRequestPermissionsResult.
    val context = LocalContext.current
    val activity = context as? MainActivity
    val onRequestPermission: () -> Unit = {
        activity?.requestRuntimePermission(Manifest.permission.READ_CONTACTS) { granted ->
            if (granted) viewModel.onPermissionGranted()
        }
    }

    Scaffold(
        snackbarHost = { SnackbarHost(snackbarHostState) },
        topBar = {
            TopAppBar(
                title = { Text(stringResource(R.string.contacts_screen_title)) },
                actions = {
                    if (state.isAuthorized) {
                        var menuExpanded by remember { mutableStateOf(false) }
                        IconButton(onClick = { menuExpanded = true }) {
                            Icon(Icons.Filled.MoreVert, stringResource(R.string.contacts_options_cd))
                        }
                        DropdownMenu(
                            expanded = menuExpanded,
                            onDismissRequest = { menuExpanded = false }
                        ) {
                            DropdownMenuItem(
                                text = { Text(stringResource(R.string.contacts_menu_discover_keys)) },
                                onClick = { menuExpanded = false; viewModel.startBulkScan() },
                                leadingIcon = { Icon(Icons.Filled.TravelExplore, null) },
                                enabled = !state.bulkScanActive
                            )
                            DropdownMenuItem(
                                text = { Text(stringResource(R.string.contacts_menu_auto_match)) },
                                onClick = { menuExpanded = false; viewModel.runAutoMatch() },
                                leadingIcon = { Icon(Icons.Filled.AutoFixHigh, null) }
                            )
                            DropdownMenuItem(
                                text = { Text(stringResource(R.string.contacts_menu_refresh)) },
                                onClick = { menuExpanded = false; viewModel.refreshContacts() },
                                leadingIcon = { Icon(Icons.Filled.Refresh, null) }
                            )
                            HorizontalDivider()
                            DropdownMenuItem(
                                text = { Text(stringResource(R.string.contacts_menu_reset_cache)) },
                                onClick = { menuExpanded = false; viewModel.clearCheckedEmails() },
                                leadingIcon = { Icon(Icons.Filled.ClearAll, null) }
                            )
                        }
                    }
                }
            )
        }
    ) { padding ->
        if (!state.isAuthorized) {
            // Permission request view — uses MainActivity helper
            // (A9 Fix1) instead of the broken Compose launcher.
            PermissionView(
                onRequestPermission = onRequestPermission,
                modifier = Modifier.fillMaxSize().padding(padding)
            )
        } else if (state.isLoading) {
            Box(
                modifier = Modifier.fillMaxSize().padding(padding),
                contentAlignment = Alignment.Center
            ) {
                CircularProgressIndicator()
            }
        } else if (state.contactsList.isEmpty()) {
            EmptyContactsView(
                onRefresh = { viewModel.refreshContacts() },
                modifier = Modifier.fillMaxSize().padding(padding)
            )
        } else {
            ContactsList(state = state, viewModel = viewModel, modifier = Modifier.padding(padding))
        }
    }

    // ── First-visit tooltip (Phase 4) ───────────────────────────────────
    ScreenTooltip(
        tooltipKey = "contacts_import",
        message = stringResource(R.string.contacts_tooltip_import)
    )
}

